# 별 찍기 - 7
n = int(input())

i = n - 1

# 상
for j in range(1, n + 1):
    print(" " * i, "*" * (j * 2 - 1), sep="")
    i -= 1

i = 1
# 하
for j in range(n - 1, 0, -1):
    print(" " * (i), "*" * (j * 2 - 1), sep="")
    i += 1


