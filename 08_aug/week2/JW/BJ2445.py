# 별 찍기 - 8
n = int(input())

# 상
for i in range(1, n + 1):
    print("*" * i, " " * (n - i) * 2, "*" * i, sep="")

# 하
for i in range(n - 1, 0, -1):
    print("*" * i, " " * (n - i) * 2, "*" * i, sep="")


